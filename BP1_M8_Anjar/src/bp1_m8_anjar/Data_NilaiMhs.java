    private void chose_file() {
        JFileChooser fileChooser = new JFileChooser();
        int returnValue = fileChooser.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            try {
                BufferedImage originalImage = javax.imageio.ImageIO.read(selectedFile);

                int targetWidth = 200;
                int targetHeight = 100;

                double widthScale = (double) targetWidth / originalImage.getWidth();
                double heightScale = (double) targetHeight / originalImage.getHeight();
                double scale = Math.min(widthScale, heightScale);

                int newWidth = (int) (originalImage.getWidth() * scale);
                int newHeight = (int) (originalImage.getHeight() * scale);

                BufferedImage resizedImage = new BufferedImage(newWidth, newHeight, BufferedImage.TYPE_INT_ARGB);
                Graphics2D g2d = resizedImage.createGraphics();
                g2d.drawImage(originalImage, 0, 0, newWidth, newHeight, null);
                g2d.dispose();

                jLabel7.setText(selectedFile.getName());
                jLabel7.setIcon(new ImageIcon(resizedImage));

                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                javax.imageio.ImageIO.write(resizedImage, "png", baos);
                byte[] photoBytes = baos.toByteArray();

                jTextField5.setText(java.util.Base64.getEncoder().encodeToString(photoBytes));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal memuat gambar: " + e.getMessage());
            }
        }
    }
    private void load_data() {
        Object header[] = {"NIM", "Nama", "Jenis Kelamin", "Prodi", "Kelas", "Nilai", "Foto"};
        DefaultTableModel data = new DefaultTableModel(null, header);
        jTable1.setModel(data);
        String sql = "SELECT nim, nama, jk, prodi, kelas, nilai, foto FROM tabel_nilaimhs";
        try {
            st = koneksi.con.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                String k1 = rs.getString("nim");
                String k2 = rs.getString("nama");
                String k3 = rs.getString("jk");
                String k4 = rs.getString("prodi");
                String k5 = rs.getString("kelas");
                String k6 = rs.getString("nilai");
                String k7 = rs.getString("foto");
                String k[] = {k1, k2, k3, k4, k5, k6, k7};
                data.addRow(k);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void input_data() {
        try {
            if (jTextField1.getText().isEmpty() || 
                jTextField2.getText().isEmpty() || 
                (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) ||
                jTextField3.getText().isEmpty() ||
                jTextField4.getText().isEmpty() || 
                jTextField5.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Data Harus Diisi!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            String jk = jRadioButton1.isSelected() ? jRadioButton1.getText() : jRadioButton2.getText();
            String sql = "INSERT INTO tabel_nilaimhs (nim, nama, jk, prodi, kelas, nilai, foto) VALUES ('" 
                    + jTextField1.getText() + "', '" 
                    + jTextField2.getText() + "', '" 
                    + jk + "', '" 
                    + jComboBox1.getSelectedItem() + "', '" 
                    + jTextField3.getText() + "', '" 
                    + jTextField4.getText() + "', '" 
                    + jTextField5.getText() + "')";

            int opsi = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda Akan Menyimpan Data?", "Konfirmasi",
                JOptionPane.YES_NO_OPTION);
            if (opsi == JOptionPane.YES_OPTION) {
                st = koneksi.con.createStatement();
                st.execute(sql);
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
                clear();
                load_data();
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak disimpan.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

