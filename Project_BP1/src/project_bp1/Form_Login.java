private void Login() {
    String level = (String) jComboBox1.getSelectedItem();

    if (level.equals("Admin")) {
        loginAdmin();
    } else if (level.equals("User")) {
        loginUser();
    } else {
        JOptionPane.showMessageDialog(null, "Level tidak valid. Pilih Admin atau User.", "Login", JOptionPane.ERROR_MESSAGE);
    }
}
