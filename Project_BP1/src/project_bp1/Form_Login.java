   private void loginUser() {
    String user = jTextField1.getText();
    String pass = jTextField2.getText();
    boolean loginSuccess = false;

    try {
        st = koneksi.con.createStatement();
        rs = st.executeQuery("SELECT username, password, level FROM user");
        while (rs.next()) {
            String Username = rs.getString("username");
            String Password = rs.getString("password");
            String Level = rs.getString("level");

            if (user.equals(Username) && pass.equals(Password) && Level.equals("User")) {
                loginSuccess = true;
                break;
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }

    if (user.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Username harus diisi", "Login", JOptionPane.ERROR_MESSAGE);
    } else if (pass.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Password harus diisi", "Login", JOptionPane.ERROR_MESSAGE);
    } else if (loginSuccess) {
        Menu_user Login = new Menu_user();
        Login.setVisible(true);
        setVisible(false);
        JOptionPane.showMessageDialog(null, "Login Sukses sebagai : " + user, "Login", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(null, "Login Gagal: Username atau Password salah, atau level tidak sesuai.", "Login", JOptionPane.ERROR_MESSAGE);
    }
}

